    private void photoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_photoActionPerformed
        try {
            JFileChooser chooser = new JFileChooser();
            chooser.showOpenDialog(null);
            
            File file = chooser.getSelectedFile();
            imagePath = file.getAbsolutePath();

            //check the image type
            if(!imagePath.contains(".JPEG") && !imagePath.contains(".JPG") && !imagePath.contains(".PNG")
                    && !imagePath.contains(".jpeg") && !imagePath.contains(".jpg") && !imagePath.contains(".png")){
                imagePath = "";
                throw new InputException();
            } else {
                studentPic.setIcon(new javax.swing.ImageIcon(imagePath));
            }
            
        } catch (InputException ex){
            Alert.viewErrorMessage("Image type not valid .. ! \nThe valid types : JPEG - JPG - PNG");
            Logger.getLogger(Add_EditStudentUI.class).error(ex);
        } catch (HeadlessException ex){
            Alert.viewErrorMessage("You didn't select any file");
            Logger.getLogger(Add_EditStudentUI.class).error(ex);
        } catch (java.lang.NullPointerException ex){
            Logger.getLogger(Add_EditStudentUI.class).error(ex);
        }
    }//GEN-LAST:event_photoActionPerformed

