    //Get data from all fields and return in students object
    private Students getInputs(){
        try {
            Students s = new Students();
            s.setAddress(address.getText());
            s.setBirthday(birthYear.getSelectedItem() + "-" + (birthMonth.getSelectedIndex() + 1) + "-" + birthDay.getSelectedItem());
            s.setAdmissionDate(admissionYear.getSelectedItem() + "-" + (admissionMonth.getSelectedIndex() + 1) + "-" + admissionDay.getSelectedItem());
            s.setEducationStage(stage.getSelectedIndex() + 1);
            s.setFirstName(first_name.getText());
            if (maleRadio.isSelected() == true) {
                s.setGender(1);
            } else {
                s.setGender(2);
            }
            s.setGuardianName(guadian_name.getText());
            s.setId(Integer.parseInt(std_id.getText()));
            s.setLastName(last_name.getText());
            s.setMiddleName(middle_name.getText());
            s.setNotes(notes.getText());
            s.setPhone(phone.getText());
            return s;
        } catch (Exception ex) {
            Alert.viewErrorMessage("There's an error .. !");
            Logger.getLogger(Add_EditStudentUI.class).error(ex);
        }
        return null;        
    }

    //Get Data from another interface and put it in fields
    protected void setInputs(Students s){
        std_id.setText(s.getId().toString());
        address.setText(s.getAddress());
        
        String[] admission = s.getAdmissionDate().split("-");
        admissionYear.setSelectedItem(admission[0]);
        admissionMonth.setSelectedIndex(Integer.parseInt(admission[1]) - 1);
        admissionDay.setSelectedIndex(Integer.parseInt(admission[2]) - 1);

        String[] birth = s.getBirthday().split("-");
        birthYear.setSelectedItem(birth[0]);
        birthMonth.setSelectedIndex(Integer.parseInt(birth[1]) - 1);
        birthDay.setSelectedIndex(Integer.parseInt(birth[2]) - 1);
        
        first_name.setText(s.getFirstName());
        middle_name.setText(s.getMiddleName());
        last_name.setText(s.getLastName());
        guadian_name.setText(s.getGuardianName());
        notes.setText(s.getNotes());
        stage.setSelectedIndex(s.getEducationStage()-1);
        phone.setText(s.getPhone());
        if (s.getGender() == 1){
            maleRadio.setSelected(true);
        } else {
            femaleRadio.setSelected(true);
        }
        try{
            File check = new File("image\\pp_"+std_id.getText()+".jpg");
            if (check.exists()) {
                studentPic.setIcon(new javax.swing.ImageIcon(check.getAbsolutePath()));
            } else {
                studentPic.setIcon(new javax.swing.ImageIcon("image\\student.png"));
            }
        } catch(Exception ex){
            Alert.viewErrorMessage("There's an error ... !");
            Logger.getLogger(Add_EditStudentUI.class).error(ex);
        }
    }
    
    //Check fields pattren and if any field don't match throw in exception
    private void checkFields() throws InputException {
        if(!std_id.getText().matches("[0-9]{3}[0-9]+")){
            throw new InputException("ID Must be at least 4 numbers\nID");
        }
        if(!first_name.getText().matches("[a-zA-Z]{2}[a-zA-Z]+")){
            throw new InputException("First Name");        
        }
        if(!middle_name.getText().matches("[a-zA-Z]{2}[a-zA-Z]+")){
            throw new InputException("Middle Name");
        }
        if(!last_name.getText().matches("[a-zA-Z]{2}[a-zA-Z]+")){
            throw new InputException("Last Name");
        }
        if(!guadian_name.getText().matches("[a-zA-Z]{2}[a-zA-Z]+\\s*[a-zA-z]+")){
            throw new InputException("Parent Name");
        }
        if(!phone.getText().matches("(2189|09)[1245][0-9]{7}")){
            throw new InputException("Phone");
        }
    }
    
    
    //Add & update student image
    private void addImageToStudent(){
        File check = new File("image");
        if (!check.exists()) {
            check.mkdir();
        }

        check = new File("image\\pp_"+std_id.getText()+".jpg");
        if (check.exists()) {
            check.delete();
        }
        
        //Copy image file to current program folder in images
        BufferedInputStream fis;
        BufferedOutputStream fos;
        try {
            fis = new BufferedInputStream(new FileInputStream(imagePath));
            fos = new BufferedOutputStream(new FileOutputStream("image\\pp_" + std_id.getText()+ ".jpg"));
            int d;
            while ((d = fis.read()) != -1) {
                fos.write(d);
            }
            fis.close();
            fos.close();
        } catch (FileNotFoundException ex){
            Alert.viewWarningMessage("File can't be found");
            Logger.getLogger(Add_EditStudentUI.class).error(ex);
        } catch (IOException ex){
            Alert.viewWarningMessage("There's an error");
            Logger.getLogger(Add_EditStudentUI.class).error(ex);
        }
    }
    
