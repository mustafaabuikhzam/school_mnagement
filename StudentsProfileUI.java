    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        Subjects sub = subjects.get(subjectsTable.getSelectedRow());
        sub.setFirst_month(Integer.parseInt(first_month.getText()));
        sub.setSecond_month(Integer.parseInt(second_month.getText()));
        sub.setThird_month(Integer.parseInt(third_month.getText()));
        sub.setMidterm(Integer.parseInt(midterm.getText()));
        sub.setFinalExam(Integer.parseInt(finalExam.getText()));
        StudentsController.studentSubMarksUpdate(Integer.parseInt(std_id.getText()),
                                                                    grade.getSelectedIndex()+1,
                                                                    semester.getSelectedIndex()+1,
                                                                    sub);
    }//GEN-LAST:event_updateActionPerformed

