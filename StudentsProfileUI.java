    private DefaultTableModel getStudentSubjectsTable(ArrayList<Subjects> subs) {
        String[] columnNames = {"Subject", "First Month","Second Month","Third Month", "Midterm","Final","Total"};
        DefaultTableModel dtm = new DefaultTableModel(columnNames , 0);

        dtm.setColumnCount(7);
        for(Subjects s : subs) {
            dtm.addRow(new Object[] {
                s.getSub_name(),
                s.getFirst_month(),
                s.getSecond_month(),
                s.getThird_month(),
                s.getMidterm(),
                s.getFinalExam(),
                0
            });
        }
        
        return dtm;
    }
    
    
